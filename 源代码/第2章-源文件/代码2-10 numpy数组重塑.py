# 重塑数组形状
import numpy as np
# 维度中的-1表示自动推断的意思
a = np.array([[1,2,3],
              [4,5,6]])

print('将二维数组转成一维数组', a.ravel())
# 输出 将二维数组转成一维数组 [1 2 3 4 5 6]

print('改变二维数组形状：2*3 -> 3*2 \n', a.reshape((3,2)))
# 输出
# 改变二维数组形状：2*3 -> 3*2
#  [[1 2]
#  [3 4]
#  [5 6]]

print('将二维数组转成列向量：\n', a.reshape((-1,1)))
# 输出
# 将二维数组转成列向量：
#  [[1]
#  [2]
#  [3]
#  [4]
#  [5]
#  [6]]
